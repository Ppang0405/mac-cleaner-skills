def format_size(size_bytes: int) -> str:
    if size_bytes < 1024:
        return f"{size_bytes} B"
    units = ("KB", "MB", "GB", "TB")
    value = float(size_bytes)
    for unit in units:
        value /= 1024.0
        if value < 1024.0 or unit == units[-1]:
            return f"{value:.2f} {unit}"
    return f"{value:.2f} TB"


def expand_patterns(patterns: Sequence[str]) -> List[Path]:
    paths: List[Path] = []
    for raw in patterns:
        expanded = os.path.expanduser(raw)
        matches = glob.glob(expanded)
        if matches:
            paths.extend(Path(match) for match in matches)
            continue
        if glob.has_magic(expanded):
            continue
        paths.append(Path(expanded))
    deduped: List[Path] = []
    seen: Set[str] = set()
    for path in paths:
        key = str(path.resolve(strict=False))
        if key not in seen:
            seen.add(key)
            deduped.append(path)
    return deduped


def path_size(path: Path) -> tuple[int, int]:
    if not path.exists():
        return 0, 0

    try:
        if path.is_symlink():
            return 0, 0
        if path.is_file():
            return path.stat().st_size, 0
    except OSError:
        return 0, 1

    total = 0
    errors = 0
    stack: List[Path] = [path]
    while stack:
        current = stack.pop()
        try:
            with os.scandir(current) as scan:
                for entry in scan:
                    try:
                        if entry.is_symlink():
                            continue
                        mode = entry.stat(follow_symlinks=False).st_mode
                        if stat.S_ISDIR(mode):
                            stack.append(Path(entry.path))
                        elif stat.S_ISREG(mode):
                            total += entry.stat(follow_symlinks=False).st_size
                    except OSError:
                        errors += 1
        except OSError:
            errors += 1
    return total, errors


def read_bundle_identifier(app_path: Path) -> Optional[str]:
    info_plist = app_path / "Contents" / "Info.plist"
    if not info_plist.exists():
        return None
    try:
        with info_plist.open("rb") as handle:
            payload = plistlib.load(handle)
    except Exception:  # noqa: BLE001
        return None
    bundle_id = payload.get("CFBundleIdentifier")
    if isinstance(bundle_id, str) and bundle_id.strip():
        return bundle_id.strip()
    return None


def default_app_roots() -> List[Path]:
    roots = [
        Path("/Applications"),
        Path("/System/Applications"),
        Path("/System/Library/CoreServices"),
        Path.home() / "Applications",
    ]
    roots.extend(Path(path) for path in glob.glob("/Volumes/*/Applications"))
    deduped: List[Path] = []
    seen: Set[str] = set()
    for root in roots:
        canonical = str(root.resolve(strict=False))
        if canonical in seen:
            continue
        seen.add(canonical)
        deduped.append(root)
    return deduped


def collect_installed_bundle_ids(extra_roots: Sequence[Path] = ()) -> Set[str]:
    roots = default_app_roots() + [root.expanduser() for root in extra_roots]
    deduped_roots: List[Path] = []
    seen_roots: Set[str] = set()
    for root in roots:
        canonical = str(root.resolve(strict=False))
        if canonical in seen_roots:
            continue
        seen_roots.add(canonical)
        deduped_roots.append(root)

    bundle_ids: Set[str] = set()
    for root in deduped_roots:
        if not root.exists() or not root.is_dir():
            continue
        if root.name.endswith(".app"):
            bundle_id = read_bundle_identifier(root)
            if bundle_id:
                bundle_ids.add(bundle_id)
            continue
        for current_root, dirnames, _ in os.walk(root, topdown=True):
            for dirname in list(dirnames):
                if not dirname.endswith(".app"):
                    continue
                app_path = Path(current_root) / dirname
                bundle_id = read_bundle_identifier(app_path)
                if bundle_id:
                    bundle_ids.add(bundle_id)
                dirnames.remove(dirname)
    return bundle_ids


def bundle_id_candidates(location: str, path: Path) -> List[str]:
    if location == "preferences":
        raw = path.stem
    elif location == "saved-state":
        raw = path.name[:-11] if path.name.endswith(".savedState") else path.name
    else:
        raw = path.name

    candidates: List[str] = []
    if raw.startswith("group."):
        candidates.append(raw[len("group."):])
    candidates.append(raw)

    valid: List[str] = []
    seen: Set[str] = set()
    for candidate in candidates:
        candidate = candidate.strip()
        if candidate in seen:
            continue
        if not BUNDLE_ID_RE.match(candidate):
            continue
        seen.add(candidate)
        valid.append(candidate)
    return valid


def detect_orphan_app_data(extra_app_roots: Sequence[Path] = ()) -> List[OrphanEntry]:
    installed_bundle_ids = collect_installed_bundle_ids(extra_roots=extra_app_roots)
    entries: List[OrphanEntry] = []
    for location, pattern in ORPHAN_SCAN_PATTERNS:
        for path in expand_patterns((pattern,)):
            if not path.exists():
                continue
            candidates = bundle_id_candidates(location=location, path=path)
            if not candidates:
                continue
            if any(
                candidate.startswith(prefix)
                for candidate in candidates
                for prefix in EXCLUDED_ORPHAN_BUNDLE_PREFIXES
            ):
                continue
            if any(candidate in installed_bundle_ids for candidate in candidates):
                continue
            size_bytes, errors = path_size(path)
            entries.append(
                OrphanEntry(
                    bundle_id=candidates[0],
                    location=location,
                    path=path,
                    size_bytes=size_bytes,
                    errors=errors,
                    risk="medium",
                    safe_to_delete=True,
                    notes=(
                        "No installed app bundle matched this bundle identifier in "
                        "standard app locations (/Applications, /System/Applications, "
                        "/System/Library/CoreServices, ~/Applications)."
                    ),
                )
            )
    entries.sort(key=lambda entry: (-entry.size_bytes, entry.bundle_id, str(entry.path)))
    return entries


def orphan_entries_to_scan_entries(entries: Sequence[OrphanEntry]) -> List[ScanEntry]:
    converted: List[ScanEntry] = []
    for entry in entries:
        converted.append(
            ScanEntry(
                key="orphan-app-data",
                label=f"Orphan app data: {entry.bundle_id}",
                category="orphan",
                risk=entry.risk,
                safe_to_delete=entry.safe_to_delete,
                notes=entry.notes,
                path=entry.path,
                source="orphan",
                exists=entry.path.exists(),
                size_bytes=entry.size_bytes,
                errors=entry.errors,
            )
        )
    return converted


def scan_static_targets(include_missing: bool) -> List[ScanEntry]:
    entries: List[ScanEntry] = []
    for target in STATIC_TARGETS:
        for path in expand_patterns(target.patterns):
            exists = path.exists()
            if not exists and not include_missing:
                continue
            size_bytes, errors = path_size(path) if exists else (0, 0)
            entries.append(
                ScanEntry(
                    key=target.key,
                    label=target.label,
                    category=target.category,
                    risk=target.risk,
                    safe_to_delete=target.safe_to_delete,
                    notes=target.notes,
                    path=path,
                    source="static",
                    exists=exists,
                    size_bytes=size_bytes,
                    errors=errors,
                )
            )
    return entries


def scan_home_dotdir_targets(include_missing: bool) -> List[ScanEntry]:
    entries: List[ScanEntry] = []
    for target in HOME_DOTDIR_TARGETS:
        for path in expand_patterns(target.patterns):
            exists = path.exists()
            if not exists and not include_missing:
                continue
            size_bytes, errors = path_size(path) if exists else (0, 0)
            entries.append(
                ScanEntry(
                    key=target.key,
                    label=target.label,
                    category=target.category,
                    risk=target.risk,
                    safe_to_delete=target.safe_to_delete,
                    notes=target.notes,
                    path=path,
                    source="home-dotdir",
                    exists=exists,
                    size_bytes=size_bytes,
                    errors=errors,
                )
            )
    return entries


def scan_project_targets(project_roots: Sequence[Path]) -> List[ScanEntry]:
    entries: List[ScanEntry] = []
    for root in project_roots:
        normalized_root = root.expanduser().resolve()
        if not normalized_root.exists() or not normalized_root.is_dir():
            print(f"[WARN] Project root not found or not a directory: {normalized_root}")
            continue

        for current_root, dirnames, _ in os.walk(normalized_root, topdown=True):
            dirnames[:] = [d for d in dirnames if d not in SKIP_WALK_DIRS]
            matched: List[str] = []
            for dirname in list(dirnames):
                if dirname not in PROJECT_DIR_POLICIES:
                    continue
                matched.append(dirname)
                dirnames.remove(dirname)

            for dirname in matched:
                found = Path(current_root) / dirname
                policy = PROJECT_DIR_POLICIES[dirname]
                size_bytes, errors = path_size(found)
                entries.append(
                    ScanEntry(
                        key=str(policy["key"]),
                        label=f"Project cache: {dirname}",
                        category="project",
                        risk=str(policy["risk"]),
                        safe_to_delete=bool(policy["safe_to_delete"]),
                        notes=str(policy["notes"]),
                        path=found,
                        source="project",
                        exists=True,
                        size_bytes=size_bytes,
                        errors=errors,
                    )
                )
    return entries


def scan_all(
    project_roots: Sequence[Path],
    include_missing: bool,
    include_home_dotdirs: bool,
    include_orphans: bool,
    orphan_app_roots: Sequence[Path] = (),
) -> List[ScanEntry]:
    entries = scan_static_targets(include_missing=include_missing)
    if include_home_dotdirs:
        entries.extend(scan_home_dotdir_targets(include_missing=include_missing))
    if project_roots:
        entries.extend(scan_project_targets(project_roots=project_roots))
    if include_orphans:
        entries.extend(
            orphan_entries_to_scan_entries(detect_orphan_app_data(extra_app_roots=orphan_app_roots))
        )
    entries.sort(
        key=lambda item: (
            -item.size_bytes,
            RISK_ORDER.get(item.risk, 99),
            str(item.path),
        )
    )
    return entries


def print_report(entries: Sequence[ScanEntry], min_size_bytes: int) -> None:
    filtered = [item for item in entries if item.size_bytes >= min_size_bytes]
    if not filtered:
        print("No matching cache targets found.")
        return

    key_width = max(6, max(len(item.key) for item in filtered))
    risk_width = max(4, max(len(item.risk) for item in filtered))
    safe_width = 4
    print(
        f"{'SIZE':>10}  {'RISK':<{risk_width}}  {'SAFE':<{safe_width}}  {'KEY':<{key_width}}  PATH"
    )
    print("-" * 120)
    for item in filtered:
        safe = "yes" if item.safe_to_delete else "no"
        print(
            f"{format_size(item.size_bytes):>10}  {item.risk:<{risk_width}}  {safe:<{safe_width}}  "
            f"{item.key:<{key_width}}  {item.path}"
        )
        print(f"{'':>10}  {'':<{risk_width}}  {'':<{safe_width}}  {'':<{key_width}}  note: {item.notes}")
        if item.errors:
            print(
                f"{'':>10}  {'':<{risk_width}}  {'':<{safe_width}}  {'':<{key_width}}  "
                f"warn: size scan had {item.errors} permission/read errors"
            )


def write_json(entries: Sequence[ScanEntry], output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    payload = [entry.to_json() for entry in entries]
    output_path.write_text(json.dumps(payload, indent=2))
    print(f"Wrote JSON report: {output_path}")


