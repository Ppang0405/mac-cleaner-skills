@dataclass(frozen=True)
class TargetSpec:
    key: str
    label: str
    category: str
    risk: str
    safe_to_delete: bool
    notes: str
    patterns: Sequence[str]


@dataclass
class ScanEntry:
    key: str
    label: str
    category: str
    risk: str
    safe_to_delete: bool
    notes: str
    path: Path
    source: str
    exists: bool
    size_bytes: int
    errors: int

    def to_json(self) -> Dict[str, object]:
        return {
            "key": self.key,
            "label": self.label,
            "category": self.category,
            "risk": self.risk,
            "safe_to_delete": self.safe_to_delete,
            "notes": self.notes,
            "path": str(self.path),
            "source": self.source,
            "exists": self.exists,
            "size_bytes": self.size_bytes,
            "size_human": format_size(self.size_bytes),
            "errors": self.errors,
        }


@dataclass
class OrphanEntry:
    bundle_id: str
    location: str
    path: Path
    size_bytes: int
    errors: int
    risk: str
    safe_to_delete: bool
    notes: str

    def to_json(self) -> Dict[str, object]:
        return {
            "bundle_id": self.bundle_id,
            "location": self.location,
            "path": str(self.path),
            "size_bytes": self.size_bytes,
            "size_human": format_size(self.size_bytes),
            "errors": self.errors,
            "risk": self.risk,
            "safe_to_delete": self.safe_to_delete,
            "notes": self.notes,
        }


