def build_known_path_index() -> Dict[str, TargetSpec]:
    index: Dict[str, TargetSpec] = {}
    for target in list(STATIC_TARGETS) + list(HOME_DOTDIR_TARGETS):
        for path in expand_patterns(target.patterns):
            index[str(path.resolve(strict=False))] = target
    return index


def total_unique_size(entries: Sequence[ScanEntry]) -> int:
    ordered = sorted(
        entries,
        key=lambda item: (
            len(item.path.resolve(strict=False).parts),
            str(item.path.resolve(strict=False)),
        ),
    )
    selected_paths: List[Path] = []
    total = 0
    for entry in ordered:
        resolved = entry.path.resolve(strict=False)
        if any(is_relative_to(resolved, parent) for parent in selected_paths):
            continue
        selected_paths.append(resolved)
        total += entry.size_bytes
    return total


def is_relative_to(path: Path, parent: Path) -> bool:
    try:
        path.relative_to(parent)
        return True
    except ValueError:
        return False


def prune_nested_entries(entries: Sequence[ScanEntry]) -> List[ScanEntry]:
    resolved = [item.path.resolve(strict=False) for item in entries]
    keep_flags = [True] * len(entries)

    for i, path_i in enumerate(resolved):
        for j, path_j in enumerate(resolved):
            if i == j:
                continue
            if path_i == path_j:
                continue
            if is_relative_to(path_i, path_j):
                keep_flags[i] = False
                print(
                    "[INFO] Skipping nested selection because parent path is also selected: "
                    f"{entries[i].path} (parent: {entries[j].path})"
                )
                break

    return [item for item, keep in zip(entries, keep_flags) if keep]


def select_entries(
    entries: Sequence[ScanEntry],
    selected_keys: Sequence[str],
    selected_paths: Sequence[str],
    allow_unknown_paths: bool,
    static_index: Dict[str, TargetSpec],
) -> List[ScanEntry]:
    selected: List[ScanEntry] = []

    for key in selected_keys:
        matched = [item for item in entries if item.key == key]
        if not matched:
            print(f"[WARN] No scan entries found for key: {key}")
            continue
        selected.extend(matched)

    known_by_path: Dict[str, ScanEntry] = {
        str(item.path.resolve(strict=False)): item for item in entries
    }
    for raw_path in selected_paths:
        path = Path(os.path.expanduser(raw_path)).resolve(strict=False)
        canonical = str(path)
        known = known_by_path.get(canonical)
        if known:
            selected.append(known)
            continue
        static_target = static_index.get(canonical)
        if static_target:
            size_bytes, errors = path_size(path)
            selected.append(
                ScanEntry(
                    key=static_target.key,
                    label=static_target.label,
                    category=static_target.category,
                    risk=static_target.risk,
                    safe_to_delete=static_target.safe_to_delete,
                    notes=static_target.notes,
                    path=path,
                    source="static-path",
                    exists=path.exists(),
                    size_bytes=size_bytes,
                    errors=errors,
                )
            )
            continue
        project_policy = PROJECT_DIR_POLICIES.get(path.name)
        if project_policy and path.exists():
            size_bytes, errors = path_size(path)
            selected.append(
                ScanEntry(
                    key=str(project_policy["key"]),
                    label=f"Project cache: {path.name}",
                    category="project",
                    risk=str(project_policy["risk"]),
                    safe_to_delete=bool(project_policy["safe_to_delete"]),
                    notes=str(project_policy["notes"]),
                    path=path,
                    source="path-hint",
                    exists=True,
                    size_bytes=size_bytes,
                    errors=errors,
                )
            )
            continue
        if not allow_unknown_paths:
            print(
                "[WARN] Skipping unknown path (not present in current scan): "
                f"{canonical}. Use --allow-unknown-paths to override."
            )
            continue
        size_bytes, errors = path_size(path)
        selected.append(
            ScanEntry(
                key="custom-path",
                label="Custom path",
                category="custom",
                risk="unknown",
                safe_to_delete=False,
                notes="User-provided path outside scanned targets.",
                path=path,
                source="custom",
                exists=path.exists(),
                size_bytes=size_bytes,
                errors=errors,
            )
        )

    deduped: List[ScanEntry] = []
    seen: Set[str] = set()
    for item in selected:
        canonical = str(item.path.resolve(strict=False))
        if canonical in seen:
            continue
        seen.add(canonical)
        deduped.append(item)
    return prune_nested_entries(deduped)


def is_protected(path: Path) -> bool:
    resolved = path.resolve(strict=False)
    home = Path.home().resolve(strict=False)
    protected = {
        Path("/"),
        home,
        Path("/System"),
        Path("/Applications"),
        Path("/Users"),
        Path("/Volumes"),
        Path("/Library"),
    }
    return resolved in protected


def delete_path(path: Path) -> tuple[bool, str, int]:
    resolved = path.resolve(strict=False)
    if is_protected(resolved):
        return False, "refused: protected path", 0
    if not resolved.exists():
        return True, "skipped: already missing", 0

    reclaimable_size, _ = path_size(resolved)

    try:
        if resolved.is_symlink() or resolved.is_file():
            resolved.unlink()
            return True, "deleted file/symlink", reclaimable_size
        if resolved.is_dir():
            shutil.rmtree(resolved)
            return True, "deleted directory tree", reclaimable_size
        return False, "unsupported path type", 0
    except Exception as exc:  # noqa: BLE001
        return False, f"delete failed: {exc}", 0


def needs_strict_risk_confirmation(entries: Sequence[ScanEntry]) -> bool:
    for item in entries:
        if item.risk in {"high", "unknown"}:
            return True
        if not item.safe_to_delete:
            return True
    return False


def command_scan(args: argparse.Namespace) -> int:
    project_roots = [Path(item) for item in args.project_root]
    orphan_app_roots = [Path(item) for item in args.app_root]
    entries = scan_all(
        project_roots=project_roots,
        include_missing=args.include_missing,
        include_home_dotdirs=args.include_home_dotdirs,
        include_orphans=args.include_orphans,
        orphan_app_roots=orphan_app_roots,
    )
    min_size_bytes = int(args.min_size_mb * 1024 * 1024)

    print_report(entries=entries, min_size_bytes=min_size_bytes)
    filtered = [item for item in entries if item.size_bytes >= min_size_bytes]
    total_bytes = total_unique_size(filtered)
    print(f"\nTotal in report: {format_size(total_bytes)}")

    if args.json_out:
        write_json(entries=entries, output_path=Path(args.json_out).expanduser())
    return 0


def command_clean(args: argparse.Namespace) -> int:
    if not args.key and not args.path:
        print("Provide at least one --key or --path for cleanup.")
        return 2
    if args.apply and not args.yes:
        print("Real deletion requires --apply plus --yes.")
        return 2

    entries: List[ScanEntry] = []
    if args.key:
        project_roots = [Path(item) for item in args.project_root]
        entries = scan_all(
            project_roots=project_roots,
            include_missing=False,
            include_home_dotdirs=args.include_home_dotdirs,
            include_orphans=False,
        )
    static_index = build_known_path_index()
    selected = select_entries(
        entries=entries,
        selected_keys=args.key,
        selected_paths=args.path,
        allow_unknown_paths=args.allow_unknown_paths,
        static_index=static_index,
    )
    if not selected:
        print("No cleanup targets selected after filtering.")
        return 2

    print("Selected cleanup targets:")
    for item in selected:
        safe = "yes" if item.safe_to_delete else "no"
        print(
            f"- {item.path} | size={format_size(item.size_bytes)} | risk={item.risk} | safe={safe} | key={item.key}"
        )
        print(f"  note: {item.notes}")

    total_size = sum(item.size_bytes for item in selected)
    print(f"\nEstimated reclaimable space: {format_size(total_size)}")

    requires_risk_token = needs_strict_risk_confirmation(selected)
    if requires_risk_token:
        print(
            f"Selected targets include high-risk/non-safe paths. Strict token required for apply mode: {RISK_CONFIRM_TOKEN}"
        )

    if not args.apply:
        print(
            "\nDry run only. Re-run with --apply --yes to actually delete the selected targets."
        )
        return 0
    if requires_risk_token and args.confirm_risk != RISK_CONFIRM_TOKEN:
        print(
            "Refusing apply mode for high-risk/non-safe targets without strict confirmation token. "
            f"Use --confirm-risk {RISK_CONFIRM_TOKEN}"
        )
        return 2

    reclaimed = 0
    failures = 0
    for item in selected:
        ok, message, reclaimed_bytes = delete_path(item.path)
        if ok:
            reclaimed += reclaimed_bytes
            print(f"[OK] {item.path}: {message}")
        else:
            failures += 1
            print(f"[ERR] {item.path}: {message}")

    print(f"\nReclaimed estimate: {format_size(reclaimed)}")
    if failures:
        print(f"Cleanup finished with {failures} failure(s).")
        return 1

    print("Cleanup finished successfully.")
    return 0


def command_scan_orphans(args: argparse.Namespace) -> int:
    app_roots = [Path(item) for item in args.app_root]
    entries = detect_orphan_app_data(extra_app_roots=app_roots)
    min_size_bytes = int(args.min_size_mb * 1024 * 1024)
    print_orphan_report(entries=entries, min_size_bytes=min_size_bytes)
    total_bytes = sum(item.size_bytes for item in entries if item.size_bytes >= min_size_bytes)
    print(f"\nTotal orphan data in report: {format_size(total_bytes)}")
    if args.json_out:
        write_orphan_json(entries=entries, output_path=Path(args.json_out).expanduser())
    return 0


def command_clean_orphans(args: argparse.Namespace) -> int:
    if not args.bundle_id and not args.path and not args.all:
        print("Choose orphan targets with --bundle-id/--path, or use --all.")
        return 2
    if args.apply and not args.yes:
        print("Real deletion requires --apply plus --yes.")
        return 2
    if args.apply and args.confirm_orphans != ORPHAN_CONFIRM_TOKEN:
        print(
            "Refusing orphan cleanup without strict confirmation token. "
            f"Use --confirm-orphans {ORPHAN_CONFIRM_TOKEN}"
        )
        return 2

    app_roots = [Path(item) for item in args.app_root]
    entries = detect_orphan_app_data(extra_app_roots=app_roots)
    min_size_bytes = int(args.min_size_mb * 1024 * 1024)
    filtered = [item for item in entries if item.size_bytes >= min_size_bytes]

    bundle_filter = set(args.bundle_id)
    path_filter = {
        str(Path(os.path.expanduser(raw_path)).resolve(strict=False)) for raw_path in args.path
    }

    selected: List[ScanEntry] = []
    for item in filtered:
        selected_by_bundle = bool(bundle_filter) and item.bundle_id in bundle_filter
        selected_by_path = bool(path_filter) and str(item.path.resolve(strict=False)) in path_filter
        selected_by_all = args.all
        if not (selected_by_bundle or selected_by_path or selected_by_all):
            continue
        selected.append(
            ScanEntry(
                key="orphan-app-data",
                label=f"Orphan app data: {item.bundle_id}",
                category="orphan",
                risk=item.risk,
                safe_to_delete=item.safe_to_delete,
                notes=item.notes,
                path=item.path,
                source="orphan",
                exists=item.path.exists(),
                size_bytes=item.size_bytes,
                errors=item.errors,
            )
        )

    selected = prune_nested_entries(selected)
    if not selected:
        print("No orphan cleanup targets matched your filters.")
        return 2

    print("Selected orphan cleanup targets:")
    for item in selected:
        print(
            f"- {item.path} | size={format_size(item.size_bytes)} | risk={item.risk} | safe=yes | key={item.key}"
        )
        print(f"  note: {item.notes}")
    total_size = sum(item.size_bytes for item in selected)
    print(f"\nEstimated reclaimable orphan space: {format_size(total_size)}")

    if not args.apply:
        print(
            "\nDry run only. Re-run with --apply --yes "
            f"--confirm-orphans {ORPHAN_CONFIRM_TOKEN} to actually delete."
        )
        return 0

    reclaimed = 0
    failures = 0
    for item in selected:
        ok, message, reclaimed_bytes = delete_path(item.path)
        if ok:
            reclaimed += reclaimed_bytes
            print(f"[OK] {item.path}: {message}")
        else:
            failures += 1
            print(f"[ERR] {item.path}: {message}")

    print(f"\nReclaimed orphan estimate: {format_size(reclaimed)}")
    if failures:
        print(f"Orphan cleanup finished with {failures} failure(s).")
        return 1
    print("Orphan cleanup finished successfully.")
    return 0


